$(document).ready(function() {
    // Handle delete button click
    $('.deleteBtn').click(function() {
        if (confirm('Are you sure you want to delete this knowledge base entry?')) {
            var id = $(this).data('id');

            // Send AJAX request to delete_knowledgebase.php
            $.ajax({
                url: 'delete_knowledgebase.php',
                type: 'POST',
                data: { id: id },
                success: function(response) {
                    // Refresh the table after successful deletion
                    $('#knowledgebaseTable').load(location.href + ' #knowledgebaseTable');
                    alert('Knowledge base entry deleted successfully.');
                    location.reload();
                },
                error: function(xhr, status, error) {
                    alert('Error deleting knowledge base entry.');
                }
            });
        }
    });
});
</script>
